    private void jButton18ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton18ActionPerformed
        // TODO add your handling code here:
        //viajes.BD.Clientes temp = (viajes.BD.Clientes) clientes.buscar(this.jTextField17.getText(), 0);
            if (!this.jTextField17.getText().equals("") && 
                    !this.jTextField5.getText().equals("") &&
                    !this.jTextField18.getText().equals("") &&
                    !this.jTextField19.getText().equals("") &&
                    !this.jTextField6.getText().equals("") &&
                    !this.jTextField20.getText().equals("") &&
                    !this.jTextField21.getText().equals("") &&
                    !this.jTextField22.getText().equals("")){
                MClientes mclientes = new MClientes();
                if (this.jButton18.getText().equals("Guardar")){
                    viajes.BD.Clientes nuevo = new viajes.BD.Clientes(this.jTextField17.getText(),
                            this.jTextField5.getText(),
                            this.jTextField18.getText(),
                            this.jTextField19.getText(),
                            this.jTextField6.getText(),
                            this.jTextField20.getText(),
                            this.jTextField21.getText(),
                            this.jTextField22.getText(),mclientes.FRECUENCIA_UNO,"");
                    clientes.agregarAlFinal(nuevo);
                    viajes.BD.EntidadFinanciera temp = (viajes.BD.EntidadFinanciera) entidad.buscar(this.jTextField19.getText(), 3);
                    if (!entidad.buscar(temp)){
                        JOptionPane.showMessageDialog(null,"La Tarjeta asociada a Cliente no existe, proceda a crearla", "Negocios - Clientes",JOptionPane.INFORMATION_MESSAGE);
                    }
                } else {
                    viajes.BD.Clientes temp = (viajes.BD.Clientes) clientes.buscar(this.jTextField17.getText(), 0);
                    viajes.BD.Clientes nuevo = new viajes.BD.Clientes(this.jTextField17.getText(),
                            this.jTextField5.getText(),
                            this.jTextField18.getText(),
                            this.jTextField19.getText(),
                            this.jTextField6.getText(),
                            this.jTextField20.getText(),
                            this.jTextField21.getText(),
                            this.jTextField22.getText(),temp.cpfrecuencia,temp.cpmonto);
                    clientes.editarPorReferencia(temp, nuevo);
                }
            } else {
                JOptionPane.showMessageDialog(null,"Debe llenar TODOS los campos", "Negocios - Clientes",JOptionPane.INFORMATION_MESSAGE);
            }
            this.jButton18.setText("Guardar");
    }//GEN-LAST:event_jButton18ActionPerformed

