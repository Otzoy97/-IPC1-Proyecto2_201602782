    private void jTextField9FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField9FocusLost
        // TODO add your handling code here:
        if (this.jTextField9.getText()!=""){
            viajes.BD.Clientes cliente = (viajes.BD.Clientes) clientes.buscar(this.jTextField9.getText(), 0);
            if (clientes.buscar(cliente)){
                this.jTextField10.setText(cliente.nombre+", "+cliente.apellido);
                this.jTextField11.setText(cliente.notarjeta);
                this.jTextField12.setText(cliente.telefono);
                this.jTextField13.setText(cliente.celular);
                this.jTextField14.setText(cliente.domicilio);
                this.jTextField15.setText(cliente.cpfrecuencia);
                this.jTextField16.setText(cliente.cpmonto);
            } else {
                String idnuevo = this.jTextField9.getText();
                int option = JOptionPane.showConfirmDialog(null, "Cliente no existe, ¿Desea agregarlo?","Cliente no encontrado",JOptionPane.YES_NO_OPTION);
                if (option == JOptionPane.YES_OPTION){
                    jTabbedPane1.setSelectedIndex(2);
                    jTabbedPane2.setSelectedIndex(0);
                    this.jTextField17.setText(idnuevo);
                }
            }
        }
    }//GEN-LAST:event_jTextField9FocusLost

